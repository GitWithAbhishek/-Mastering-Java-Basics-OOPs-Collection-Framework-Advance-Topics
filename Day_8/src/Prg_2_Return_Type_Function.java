public class Prg_2_Return_Type_Function {
    static int add(int a,int b){
        return a+b;
    }
    public static void main(String[] args) {
        int result;
        //We Need To Store The Return Value Of The Function
        result=add(4,6);
        System.out.println("The Sum Of 4 And 6 Is "+result );

    }
}
