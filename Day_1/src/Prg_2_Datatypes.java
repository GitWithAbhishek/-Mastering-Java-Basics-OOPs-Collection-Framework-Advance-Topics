public class Prg_2_Datatypes {
    public static void main(String[] args) {


        byte by=12;
        System.out.println("The Datatype Of Value "+by+" Is Byte");
        short s=20;
        System.out.println("The Datatype Of Value "+s+" Is Short");
        long l=882837837;
        System.out.println("The Datatype Of Value "+l+" Is Long");
        int i=1;
        System.out.println("The Datatype Of Value "+i+" Is Integer");
        float f=20.33f;
        System.out.println("The Datatype Of Value "+f+" Is Float");
        double d=100.292323;
        System.out.println("The Datatype Of Value "+d+" Is Double");
        char c ='A';
        System.out.println("The Datatype Of Value "+c+" Is Character");
        boolean a=true;
        System.out.println("The Datatype Of Value "+a+" Is Boolean");

    }
}
